function triangle(i::Integer,
                  parent::ArbField = RealField(64))
    i >= 1 && i <= 10 || throw(ArgumentError("give 1 ≤ i ≤ 10 not i = $i"))

    angles = [(3//4, 1//3, 1//2),
              (2//3, 1//3, 1//2),
              (2//3, 1//4, 1//2),
              (2//3, 1//3, 1//3),
              (3//4, 1//4, 1//3),
              (2//3, 1//4, 1//4),
              (2//3, 3//4, 3//4),
              (2//3, 2//3, 2//3),
              (1//2, 2//3, 3//4),
              (1//2, 2//3, 2//3),
              ]

    r = parent(1//8)
    λs = ball.(parent.(["12.400051652843377905",
                        "13.744355213213231835",
                        "20.571973537984730557",
                        "21.309407630190445260",
                        "24.456913796299111694",
                        "49.109945263284609920",
                        "4.261735",
                        "5.159146",
                        "6.241748",
                        "6.777108",
                        ]), r)

    (domain = SphericalTriangle(fmpq.(angles[i]), parent), λ = λs[i])
end
